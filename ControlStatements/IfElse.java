package ControlStatements;

public class IfElse {
			public static void main(String[] args) {
				int marks = 85;

		        // if-else statement to check pass/fail
		        if (marks >= 50) {
		            System.out.println("You passed!");
		        } 
	            // Otherwise, print "You failed."
		        else {
		            System.out.println("You failed.");
		        }
			}
}
