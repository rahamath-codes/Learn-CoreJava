package LoopingStatements;

public class ForLoop {
    // Entry Check Loop - Checks Condition before loop/iteration gets started.
	 public static void main(String[] args) {
	        // Print numbers from 1 to 5
	        for (int i = 1; i <= 5; i++) {
	            System.out.println("i = " + i);
	        }
	    }
}
