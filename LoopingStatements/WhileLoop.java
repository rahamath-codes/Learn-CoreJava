package LoopingStatements;

public class WhileLoop {
	public static void main(String[] args) {
        int i = 1;
        // Entry Check Loop - Checks Condition before loop/iteration gets started.	
        // Run loop as long as i is less than or equal to 5
        while (i <= 5) {
            System.out.println("i = " + i);
            i++;
        }
    }
}	
